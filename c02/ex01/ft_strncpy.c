char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	n1;

	n1 = 0;
	while (src[n1] != '\0' && n1 < n)
	{
		dest[n1] = src[n1];
		n1++;
	}
	while (n1 < n)
	{
		dest[n1] = '\0';
		n1++;
	}
	return (dest);
}
