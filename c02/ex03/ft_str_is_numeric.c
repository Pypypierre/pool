int	ft_str_is_numeric(char *str)
{
	int	n;
	int	check;

	n = 0;
	check = 1;
	while (str[n] != '\0')
	{
		if (!(str[n] >= 48 && str[n] <= 57))
		{
			check = 0;
		}
		n++;
	}
	return (check);
}
