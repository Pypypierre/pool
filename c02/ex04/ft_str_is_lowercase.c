int	ft_str_is_lowercase(char *src)
{
	int	n;
	int	check;

	n = 0;
	check = 1;
	while (src[n] != '\0')
	{
		if (!(src[n] >= 97 && src[n] <= 122))
		{
			check = 0;
		}
		n++;
	}
	return (check);
}
